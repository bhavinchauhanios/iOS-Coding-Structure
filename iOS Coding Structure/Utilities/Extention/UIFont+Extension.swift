import UIKit



extension UIFont {

    class func circularMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "CircularStd-Book", size: size)!
    }
    class func circularStdBoldWebfont(size: CGFloat) -> UIFont {
        return UIFont(name: "CircularStd-Bold", size: size)!
    }
    class func circularStdBookWebfont(size: CGFloat) -> UIFont {
        return UIFont(name: "CircularStd-Medium", size: size)!
    }

}

